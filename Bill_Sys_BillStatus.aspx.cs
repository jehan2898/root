    protected void btnDelete_Click(object sender, EventArgs e)
    {
        string id = string.Format("Id: {0} Uri: {1}", Guid.NewGuid(), HttpContext.Current.Request.Url);
        using (Utils utility = new Utils())
        {
            utility.MethodStart(id, System.Reflection.MethodBase.GetCurrentMethod());
        }
        _deleteOpeation = new Bill_Sys_DeleteBO();
        String szListOfBillStatus = "";
        try
        {
            for (int i = 0; i < grdBillStatusNameList.Items.Count; i++)
            {
                CheckBox chkDelete1 = (CheckBox)grdBillStatusNameList.Items[i].FindControl("chkDelete");
                if (chkDelete1.Checked)
                {
                    if (!_deleteOpeation.deleteRecord("SP_MST_BILL_STATUS", "@SZ_BILL_STATUS_ID", grdBillStatusNameList.Items[i].Cells[1].Text))
                    {
                        if (szListOfBillStatus == "")
                        {
                            szListOfBillStatus = grdBillStatusNameList.Items[i].Cells[2].Text;
                        }
                        else
                        {
                            szListOfBillStatus = szListOfBillStatus + " , " + grdBillStatusNameList.Items[i].Cells[2].Text;
                        }
                    }
                }
            }
            if (szListOfBillStatus != "")
            {
                Page.ClientScript.RegisterClientScriptBlock(typeof(GridView), "Msg", "alert('Records for bill status " + szListOfBillStatus + "  exists.'); ", true);
            }
            else
            {
                lblMsg.Visible = true;
                lblMsg.Text = "Bill status deleted successfully ...";
            }
            BindGrid();
        }
        catch (Exception ex)
        {
            Elmah.ErrorSignal.FromCurrentContext().Raise(ex);
            using (Utils utility = new Utils())
            {
                utility.MethodEnd(id, System.Reflection.MethodBase.GetCurrentMethod());
            }
            string str2 = "Error Request=" + id + ".Please share with Technical support.";
            base.Response.Redirect("Bill_Sys_ErrorPage.aspx?ErrMsg=" + str2);
        }
        //Method End
        using (Utils utility = new Utils())
        {
            utility.MethodEnd(id, System.Reflection.MethodBase.GetCurrentMethod());
        }
